/**
 * osa_flush_cache_all - 刷新所有缓存
 * 返回: 无
 * 说明: 刷新整个系统的数据缓存和指令缓存，确保所有缓存数据都被写回到内存。
 */
extern void osa_flush_cache_all(void);

/**
 * osa_cpuc_flush_dcache_area - 刷新CPU数据缓存区域
 * @addr: 要刷新的地址
 * @size: 要刷新的大小
 * 返回: 无
 * 说明: 刷新指定地址范围内的数据缓存，确保该范围内的数据被写回到内存。
 */
extern void osa_cpuc_flush_dcache_area(void *addr, int size);

/**
 * osa_flush_dcache_area - 刷新数据缓存区域
 * @kvirt: 虚拟地址
 * @phys_addr: 物理地址
 * @length: 要刷新的长度
 * 返回: 无
 * 说明: 刷新指定虚拟地址和物理地址范围内的数据缓存，确保该范围内的数据被写回到内存。
 */
extern void osa_flush_dcache_area(void *kvirt, unsigned long phys_addr, unsigned long length);

/**
 * osa_flush_dcache_all - 刷新所有数据缓存
 * 返回: 成功返回0，失败返回负值
 * 说明: 刷新整个系统的数据缓存，确保所有数据缓存都被写回到内存。
 */
extern int osa_flush_dcache_all(void);

#endif // __OSA_CACHE_H__
